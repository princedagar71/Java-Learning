package com.company;
class Conditional_Operators{
    public static void main(String[] args){
        //> < >= <= == !=
        int a=10,b=5;
        if(a>b)
            System.out.println(true);
        if (a<b)
            System.out.println(true);
        if (a>=b)
            System.out.println(true);
        if (a<=b)
            System.out.println(true);
        if (a==b)
            System.out.println(true);
        if (a!=b)
            System.out.println(true);
    }
}